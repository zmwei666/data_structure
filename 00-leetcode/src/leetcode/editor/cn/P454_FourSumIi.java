class Solution {
    public int fourSumCount(int[] nums1, int[] nums2, int[] nums3, int[] nums4) {
		int res = 0;
		HashMap<Integer, Integer> map = new HashMap<>();
		for (int i : nums1) {
			for (int j : nums2) {
				int temp = i + j;
				map.put(temp, map.getOrDefault(temp, 0) + 1);
			}
		}
		for (int i : nums3) {
			for (int j : nums4) {
				res += map.getOrDefault(-i - j, 0);
			}
		}
		return res;
    }
}
